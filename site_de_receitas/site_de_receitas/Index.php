                    5 ramas de canela <br>
                    óleo para untar a assadeira <br>
                    Tempo de preparo:Até 1h <br>
                    Serve:5 unidades <br>
                    
Modo de preparo <br>
Lave e seque bem as maçãs. Espete uma rama de canela em cada uma e reserve (se preferir, utilize palitos de sorvete). Atenção: as maçãs devem estar em temperatura ambiente, se estiverem geladas ou frias podem umedecer o caramelo.
<br>Unte uma assadeira com óleo e reserve - ela vai servir de base para apoiar as maçãs do amor enquanto secam.
<br>Numa panela pequena, coloque água até metade do volume. Encaixe uma tigela de vidro refratário (ou inox) sobre a panela e leve ao fogo baixo - eles vão ser usados para o banho-maria.
<br>Em outra panela pequena, misture delicadamente o açúcar, a água e o vinagre. Cuidado para, na hora de misturar, não espirrar nenhuma gotinha do líquido na lateral da panela (caso contrário, essas pequenas gotinhas podem queimar e amargar a receita).
<br>Leve a panela com o açúcar ao fogo médio. A partir deste momento não mexa mais a calda para evitar que ela cristalize. Assim que começar a ferver, diminua o fogo e deixe cozinhar por mais 15 minutos até atingir o ponto de bala dura - neste ponto as bolhas da calda demoram mais para estourar e a espuma da superfície atinge um tom dourado. Para verificar o ponto: com uma colher de chá, retire uma porção da calda e transfira para um copo com água, você vai ouvir um estalinho e a calda vai endurecer rapidamente como uma bala.
<br>Assim que atingir o ponto, desligue o fogo e transfira a calda com cuidado para a tigela reservada do banho-maria - assim a calda demora mais para endurecer.
<br>Misture a calda com uma colher para eliminar as bolhas.
<br>Para caramelar as maçãs: segure a maçã pela rama de canela e mergulhe na calda; gire para cobrir toda a superfície e deixe escorrer bem o excesso; transfira para a assadeira untada e repita com o restante.
<br>Deixe esfriar por cerca de 15 minutos para endurecer. 10-Sirva a seguir ou armazene individualmente em saquinhos de celofane bem fechados. As maçãs permanecem crocantes e carameladas por até 3 dias.
                    
